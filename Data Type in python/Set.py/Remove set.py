
#Using Remove method


Name_of_Brazil_Players = {"Neymar","Casemiro","Vinicius Jr","Richarlison","Alisson Becker","Play"}

Name_of_Brazil_Players.remove("Neymar")

print(Name_of_Brazil_Players)

#Using Discard method

Name_of_Brazil_Players.discard("Alisson Becker")

print(Name_of_Brazil_Players)

print(Name_of_Brazil_Players.discard("Playing"))


